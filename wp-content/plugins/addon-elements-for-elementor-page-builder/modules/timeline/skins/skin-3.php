			'item_icon_icon_focus_primary_color',
			[
				'default' => '#4054b2',
			]
		);
		$this->update_control(
			'item_icon_icon_focus_secondary_color',
			[
				'default' => '#ffffff',
			]
		);
		$this->update_control(
			'item_icon_icon_border_radius',
			[
				'default' => [
					'top'    => 7,
					'right'  => 7,
					'bottom' => 7,
					'left'   => 7,
				],
			]
		);
		$this->update_control(
			'timeline_align',
			[
				'default' => 'left',
			]
		);
		$this->update_control(
			'content_align',
			[
				'default' => 'left',
			]
		);
		$this->update_control(
			'content_padding',
			[
