			'item_icon_icon_padding',
			[
				'default' => [
					'size' => 10,
				],
			]
		);
		$this->update_control(
			'arrow_align',
			[
				'default' => 'top',
			]
		);
	}
	public function register_items_control( Widget_Base $widget ) {
		$this->parent = $widget;
		//$this->bpel_timeline_content_section( $widget );
	}
