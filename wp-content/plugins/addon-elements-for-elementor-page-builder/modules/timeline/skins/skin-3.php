			]
		);
		$this->update_control(
			'progress_color',
			[
				'default' => '#d8d8d8',
			]
		);
		$this->update_control(
			'line_thickness',
			[
				'default' => [
					'size' => 5,
				],
			]
		);
		$this->update_control(
			'item_icon_icon_size',
			[
				'default' => [
					'size' => 25,
				],
			]
		);
